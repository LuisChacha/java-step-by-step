public class Comparacion {
    public static void main(String[] args) {
        double sueldo1=10000.51558;
        double sueldo2=450055.052558;
        System.out.println("sueldo1 > sueldo2: " + (sueldo1 > sueldo2));
        System.out.println("sueldo1 < sueldo2: " + (sueldo1 < sueldo2));
        System.out.println("sueldo1 >= sueldo2: " + (sueldo1 >= sueldo2));
        System.out.println("sueldo1 <= sueldo2: " + (sueldo1 <= sueldo2));
        System.out.println("sueldo1 != sueldo2: " + (sueldo1 != sueldo2));
        System.out.println("sueldo1 == sueldo2: " + (sueldo1 == sueldo2));
    }
}
